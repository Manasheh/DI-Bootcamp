function robotList () {
    robots.forEach((item) => {
        const li = document.createElement('li')
        li.classList.add('robot')
        li.innerHTML = `
        <div class = 'robot-img'>
            <img src = ${item.image} />
        </div>
        <div>
            <h3>${item.name}</h3>
            <h4>${item.username}</h4>
            <p>${item.email}</p>
        </div>
        `
        ul.appendChild(li)
        main.appendChild(ul) // section 
    })
}

